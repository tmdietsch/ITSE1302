// A constant is something, such as a string, that does not change
/** A data type classifies the data so
 that the compiler knows what to do with it **/
String firstName = "Timothy"
String lastName = "Dietsch"
/** A variable is something, like an integer,
 that can be change as many times as necessary **/
Integer elite = 1337
Integer notAsElite = elite / 2
/** A variable can be changed,
 a constant can not change **/
println elite * 2
println notAsElite + (elite / 3)
println notAsElite - 400
println elite % 10
println elite ** 2

